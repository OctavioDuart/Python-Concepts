# coding=utf-8



# Declaração
dictionary  = {
    "name" : "Octávio",
    "last_name": "Duarte",
    "profession": "Developer",
    "age": 19
}

# Acessando os Valores

print("My name : %s"  %(dictionary["name"]))      #Octávio
print("My last : %s"  %(dictionary["last_name"])) #Duarte
print("My profession %s" %(dictionary["profession"])) #Developer
print("My age  : %s"  %(dictionary["age"])) #19



'''
    Basicamente a sintaxe para acessarmos os valores de um objeto é
    objeto["chave"]
'''